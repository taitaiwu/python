class TrainValDataset(Dataset):
    """
    Supports train.txt/val.txt where image_name might be:
    - "72/72_21.jpg"
    - "train/72/72_21.jpg"
    - "val/72/72_21.jpg"
    - "72_21.jpg" (rare)
    """
    def __init__(self, root, split="train", transform=None):
        self.root = Path(root)
        self.split = split
        self.transform = transform
        self.samples = []

        txt_file = self.root / f"{split}.txt"
        if not txt_file.exists():
            raise FileNotFoundError(f"Missing: {txt_file}")

        split_dir = self.root / split
        if not split_dir.exists():
            raise FileNotFoundError(f"Missing: {split_dir}")

        with open(txt_file, "r") as f:
            for line in f:
                name, label = line.strip().split()
                rel = Path(name)

                # 先嘗試：root/name（如果 txt 已經含 train/val 前綴，這個會對）
                p1 = self.root / rel

                # 再嘗試：root/split/name（如果 txt 沒含 train/val 前綴，這個會對）
                p2 = split_dir / rel

                if p1.exists():
                    img_path = p1
                elif p2.exists():
                    img_path = p2
                else:
                    # 最後保底：只取檔名在 split 內遞迴找（慢，但只在少數壞資料列觸發）
                    matches = list(split_dir.rglob(rel.name))
                    if len(matches) > 0:
                        img_path = matches[0]
                    else:
                        raise FileNotFoundError(f"Image not found for line: {name} (tried {p1} and {p2})")

                self.samples.append((img_path, int(label)))

        print(f"[Dataset] {split}: {len(self.samples)} samples")

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        p, y = self.samples[idx]
        img = Image.open(p).convert("RGB")
        if self.transform:
            img = self.transform(img)
        return img, y

class TestDataset(Dataset):
    def __init__(self, root, transform=None):
        self.root = Path(root)
        self.transform = transform
        test_dir = self.root / "test"
        if not test_dir.exists():
            raise FileNotFoundError(f"Missing: {test_dir}")

        self.paths = sorted(test_dir.glob("*.jpg"), key=lambda p: int(p.stem))
        print(f"[Dataset] test: {len(self.paths)} images")

    def __len__(self): return len(self.paths)

    def __getitem__(self, idx):
        p = self.paths[idx]
        img = Image.open(p).convert("RGB")
        if self.transform: img = self.transform(img)
        return img, p.name

