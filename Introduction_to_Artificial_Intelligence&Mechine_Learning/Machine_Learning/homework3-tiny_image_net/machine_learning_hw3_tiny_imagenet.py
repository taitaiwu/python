class SoftTargetCrossEntropy(nn.Module):
    def __init__(self, label_smoothing=0.1):
        super().__init__()
        self.ls = label_smoothing

    def forward(self, logits, target):
        if target.dim() == 1:
            c = logits.size(1)
            with torch.no_grad():
                t = torch.zeros_like(logits).fill_(self.ls/(c-1))
                t.scatter_(1, target.unsqueeze(1), 1.0 - self.ls)
            target = t
        logp = torch.log_softmax(logits, dim=1)
        return (-target * logp).sum(dim=1).mean()

def mixup_cutmix(x, y, num_classes=100, mixup_alpha=0.2, cutmix_alpha=1.0, p=0.8):
    if random.random() > p:
        return x, y, None

    bs = x.size(0)
    idx = torch.randperm(bs, device=x.device)
    x2, y2 = x[idx], y[idx]

    use_cutmix = random.random() < 0.5
    if use_cutmix:
        lam = np.random.beta(cutmix_alpha, cutmix_alpha)
        W, H = x.size(3), x.size(2)
        cut_rat = np.sqrt(1. - lam)
        cw, ch = int(W * cut_rat), int(H * cut_rat)
        cx, cy = np.random.randint(W), np.random.randint(H)

        x1 = int(np.clip(cx - cw//2, 0, W)); x2b = int(np.clip(cx + cw//2, 0, W))
        y1 = int(np.clip(cy - ch//2, 0, H)); y2b = int(np.clip(cy + ch//2, 0, H))

        x[:, :, y1:y2b, x1:x2b] = x2[:, :, y1:y2b, x1:x2b]
        lam = 1 - ((x2b-x1)*(y2b-y1)/(W*H))
    else:
        lam = np.random.beta(mixup_alpha, mixup_alpha)
        x = x * lam + x2 * (1 - lam)

    y1 = torch.zeros((bs, num_classes), device=x.device).scatter_(1, y.view(-1,1), 1.0)
    y2oh = torch.zeros((bs, num_classes), device=x.device).scatter_(1, y2.view(-1,1), 1.0)
    ysoft = y1 * lam + y2oh * (1 - lam)
    return x, y, ysoft

class EMA:
    def __init__(self, model, decay=0.999):
        self.decay = decay
        self.shadow = {n: p.data.clone() for n,p in model.named_parameters() if p.requires_grad}
        self.backup = {}

    @torch.no_grad()
    def update(self, model):
        for n,p in model.named_parameters():
            if p.requires_grad:
                self.shadow[n] = self.shadow[n]*self.decay + p.data*(1-self.decay)

    def apply(self, model):
        self.backup = {}
        for n,p in model.named_parameters():
            if p.requires_grad:
                self.backup[n] = p.data.clone()
                p.data = self.shadow[n].clone()

    def restore(self, model):
        for n,p in model.named_parameters():
            if p.requires_grad:
                p.data = self.backup[n].clone()
        self.backup = {}

def batch_acc(logits, y):
    return (logits.argmax(1) == y).float().mean().item()

criterion_train = SoftTargetCrossEntropy(label_smoothing=0.1)
criterion_eval  = nn.CrossEntropyLoss()

optimizer = optim.AdamW(model.parameters(), lr=LR, weight_decay=0.05)

steps_per_epoch = len(train_loader)
total_steps = EPOCHS * steps_per_epoch
warmup_steps = int(0.05 * total_steps)

def lr_lambda(step):
    if step < warmup_steps:
        return step / max(1, warmup_steps)
    prog = (step - warmup_steps) / max(1, total_steps - warmup_steps)
    return 0.5 * (1 + math.cos(math.pi * prog))

scheduler = optim.lr_scheduler.LambdaLR(optimizer, lr_lambda)
scaler = torch.amp.GradScaler("cuda", enabled=(device.type == "cuda"))

# ema = EMA(model, decay=0.999)

os.makedirs(OUT_DIR, exist_ok=True)
best_val = 0.0
best_path = f"{OUT_DIR}/best.pt"

global_step = 0
for epoch in range(1, EPOCHS+1):
    t0 = time.time()
    model.train()

    tr_loss = 0.0
    tr_acc = 0.0
    n = 0

    for x, y in train_loader:
        x, y = x.to(device, non_blocking=True), y.to(device, non_blocking=True)
        x, y_hard, y_soft = mixup_cutmix(x, y, num_classes=100, mixup_alpha=0.2, cutmix_alpha=1.0, p=0.8)

        optimizer.zero_grad(set_to_none=True)
        with torch.amp.autocast("cuda", enabled=(device.type=="cuda")):
            logits = model(x)
            loss = criterion_train(logits, y_soft if y_soft is not None else y_hard)

        scaler.scale(loss).backward()
        scaler.step(optimizer)
        scaler.update()

        # ema.update(model)

        tr_loss += loss.item() * x.size(0)
        tr_acc  += batch_acc(logits.detach(), y_hard) * x.size(0)
        n += x.size(0)

        scheduler.step()
        global_step += 1

    tr_loss /= n
    tr_acc  /= n

    # ---- eval (NO EMA) ----
    model.eval()
    correct = total = 0
    val_loss = 0.0
    with torch.no_grad():
        for x, y in val_loader:
            x, y = x.to(device, non_blocking=True), y.to(device, non_blocking=True)
            logits = model(x)
            val_loss += criterion_eval(logits, y).item() * x.size(0)
            correct += (logits.argmax(1) == y).sum().item()
            total += y.size(0)
    val_loss /= total
    val_acc = correct / total

    dt = time.time() - t0
    print(f"Epoch {epoch:03d}/{EPOCHS} | train acc {tr_acc:.4f} loss {tr_loss:.4f} | val acc {val_acc:.4f} loss {val_loss:.4f} | {dt:.1f}s")

    if val_acc > best_val:
        best_val = val_acc
        torch.save({"model": model.state_dict()}, best_path)
        print("  [best] saved:", best_path)

